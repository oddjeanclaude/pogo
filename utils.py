def pa_to_psi(pa):
    return pa * 0.000145038

def k_to_f(k):
    return (k - 273.15) * 9/5 + 32

def m3_to_gal(m3):
    return m3 * 264.172

def n_to_lbf(n):
    return n * 0.224809

def kg_to_lb(kg):
    return kg * 2.20462

def psi_to_pa(psi):
    return psi / 0.000145038

def f_to_k(f):
    return (f - 32) * 5/9 + 273.15

def gal_to_m3(gal):
    return gal / 264.172

def lb_to_kg(lb):
    return lb / 2.20462

